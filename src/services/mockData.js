// Initialize local storage if empty
const loadData = (key, defaultData) => {
  const stored = localStorage.getItem(key);
  if (stored) return JSON.parse(stored);
  localStorage.setItem(key, JSON.stringify(defaultData));
  return defaultData;
};

// Rankings API
export const getRankings = () => loadData('stitch_rankings', initialRankings);
export const getRankingBySlug = (slug) => {
  return getRankings().find(r => r.slug === slug);
};
export const addRanking = (ranking) => {
  const data = getRankings();
  data.push(ranking);
  localStorage.setItem('stitch_rankings', JSON.stringify(data));
};
export const editRanking = (slug, updatedRanking) => {
  const data = getRankings().map(r => r.slug === slug ? updatedRanking : r);
  localStorage.setItem('stitch_rankings', JSON.stringify(data));
};
export const deleteRanking = (slug) => {
  const data = getRankings().filter(r => r.slug !== slug);
  localStorage.setItem('stitch_rankings', JSON.stringify(data));
};

// Universities API
export const getUniversities = () => loadData('stitch_universities_v2', initialUniversities);
export const getUniversitiesByCategory = (category) => {
  return getUniversities().filter(u => u.category === category);
};
export const addUniversity = (uni) => {
  const data = getUniversities();
  data.push({ ...uni, id: Date.now().toString() });
  localStorage.setItem('stitch_universities_v2', JSON.stringify(data));
};
export const editUniversity = (id, updatedUni) => {
  const data = getUniversities().map(u => u.id === id ? updatedUni : u);
  localStorage.setItem('stitch_universities_v2', JSON.stringify(data));
};
export const deleteUniversity = (id) => {
  const data = getUniversities().filter(u => u.id !== id);
  localStorage.setItem('stitch_universities_v2', JSON.stringify(data));
};
